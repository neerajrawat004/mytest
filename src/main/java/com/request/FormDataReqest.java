package com.request;

import org.mongodb.morphia.annotations.Entity;

@Entity("form_data")
public class FormDataReqest {

	private String name;
	private String email;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
